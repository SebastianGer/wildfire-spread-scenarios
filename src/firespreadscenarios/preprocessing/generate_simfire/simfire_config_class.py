# Changes made to the original SimFire code:
# - LandFireLatLongBox: Fix the string representation of the bounding box. The original version encodes many space characters, due to line breaks in the string definition.
# - Config: Manually set a certain area in the western USA from which locations are randomly sampled

import random
import shutil
from typing import Optional

import landfire
from simfire.utils.config import Config
from simfire.utils.layers import LandFireLatLongBox, LatLongBox


class LandFireLatLongBoxFixedBBox(LandFireLatLongBox):
    # Fixes the string representation of the bounding box. The original version encodes many space characters, due to line breaks in the string definition.
    # Original code: https://github.com/mitrefireline/simfire/blob/04905b19fe8c10dc08d64faddd00303e0561bb66/simfire/utils/layers.py#L43
    def query_lat_lon_layer_data(self):
        """
        Functionality to get the indices and values of the layers at a queried point.

        For GIS data you need to calculate the offsets to get the array indice

        Arguments:
            layer: the gdal layer to query
            band: the gdal raster band that contains the data

        """
        self.output_path.mkdir(parents=True, exist_ok=True)

        lf = landfire.Landfire(
            bbox=f"{self.points[0][1]} {self.points[0][0]} {self.points[1][1]+self.pad_distance} {self.points[1][0]-self.pad_distance}",
            output_crs="4326",
            email="",  # needs to be set
        )
        # assume the order of data retrieval stays the same
        lf.request_data(
            layers=self.layer_products.values(),
            output_path=str(self.output_path / self.product_layers_path),
        )

        shutil.unpack_archive(
            self.output_path / self.product_layers_path, self.output_path
        )


class SamplingLatLonConfig(Config):
    # Samples from a fixed area in the west of the US
    # Original code: https://github.com/mitrefireline/simfire/blob/04905b19fe8c10dc08d64faddd00303e0561bb66/simfire/utils/config.py#L294

    def _make_lat_long_box(self) -> Optional[LandFireLatLongBox]:
        """
        Optionally create the LatLongBox used by the data layers if any
        of the data layers are of type `operational`.

        Returns:
            None if none of the data layers are `operational`
            LatLongBox with the config coordinates and shape if any of
            the data layers are `operational`
        """

        # temporarily instantiate LatLongBox for the BurnProbabiltyLayer
        self.lat_long_box = LatLongBox()

        if (
            self.yaml_data["terrain"]["topography"]["type"] == "operational"
            or self.yaml_data["terrain"]["fuel"]["type"] == "operational"
        ):
            year = self.yaml_data["operational"]["year"]
            self._set_all_combos()
            tl_lat, tl_lon = None, None
            if self.yaml_data["operational"]["seed"] is not None:
                # Manually chosen bounding box area in the west of the US
                lat_range = [38.5395, 41.8815]
                lon_range = [-123.9729, -120.2813]
                lat_diff = lat_range[1] - lat_range[0]
                lon_diff = lon_range[1] - lon_range[0]

                random_gen = random.Random(self.yaml_data["operational"]["seed"])

                tl_lat = lat_range[0] + random_gen.random() * lat_diff
                tl_lon = lon_range[0] + random_gen.random() * lon_diff

            if tl_lat is None:
                tl_lat = self.yaml_data["operational"]["latitude"]
                tl_lon = self.yaml_data["operational"]["longitude"]
            height = self.yaml_data["operational"]["height"]
            width = self.yaml_data["operational"]["width"]

            # calculate the bottom right corner
            br_lat = tl_lat - ((height / 30) * 0.00027777777803598015)
            br_long = tl_lon + ((width / 30) * 0.00027777777803598015)
            valid = self._check_lat_long(((tl_lat, tl_lon), (br_lat, br_long)))
            if not valid:
                message = (
                    "Lat/Long box is not valid. Data does not "
                    "exist between the bounding box: "
                    f"({(tl_lat, tl_lon), (br_lat, br_long) }), "
                    f"and the year {year}."
                )
                raise RuntimeError(message)
            else:
                landfire_lat_long_box = LandFireLatLongBoxFixedBBox(
                    points=((tl_lat, tl_lon), (br_lat, br_long)),
                    year=year,
                    height=height,
                    width=width,
                )
            return landfire_lat_long_box
        elif (
            self.yaml_data["terrain"]["topography"]["type"] == "historical"
            or self.yaml_data["terrain"]["fuel"]["type"] == "historical"
        ):
            return self.historical_layer.lat_lon_box
        else:
            return None


